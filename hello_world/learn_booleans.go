package main

import (
	"fmt"
)

func main() {

	fmt.Println("Greater than:", 1 > 2)
	fmt.Println("Less than:", 1 < 2)
	fmt.Println("Equal to:", 1 == 2)

	fmt.Println("Greater or equal than:", 1 >= 2)
	fmt.Println("Less or equal than:", 1 <= 2)

	fmt.Println("Equality:", 1.0 == 2)
	fmt.Println("Inequality:", 2.0 != 2)
}
